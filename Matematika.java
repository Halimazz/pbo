
class Matematika{
    public int tambah(int angka1, int angka2) {
        return angka1 + angka2;
    }
    
    public double tambah(double angka1, double angka2, double angka3) {
        return angka1 + angka2 + angka3;
    }
    
    public int kurang(int angka1, int angka2) {
        return angka1 - angka2;
    }
    
    public double kurang(double angka1, double angka2, double angka3) {
        return angka1 - angka2 - angka3;
    }
    
    public int kali(int angka1, int angka2) {
        return angka1 * angka2;
    }
    
    public double kali(double angka1, double angka2, double angka3) {
        return angka1 * angka2 * angka3;
    }
    
    public int bagi(int angka1, int angka2) {
        return angka1 / angka2;
    }
    
    public double bagi(double angka1, double angka2, double angka3) {
        return angka1 / angka2 / angka3;
    }
}
